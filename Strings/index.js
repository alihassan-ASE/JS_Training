//  If else conditons syntax

let num = 20;

if(num === 20){
  console.log('Hello i am 20 years old');
}
else if (num === 30){
  console.log('hello i am 30 years old');
}
else {
  console.log('Invalid');
}

// Switch Statement

switch(num){
  case 1:
    console.log('Number is 1');
    break;
  case 10:
    console.log('Number is 10');
    break;
  default:
    console.log('By default number is 40');
}